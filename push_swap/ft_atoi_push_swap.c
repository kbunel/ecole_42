#include "push_swap.h"

void		ft_atoi_check_ps_err(const char *str)
{
	int				i;
	int				n;
	long long		a;

	i = 0;
	a = 0;
	n = 1;
	if (str[i] == '-' || str[i] == '+')
	{
		i++;
		n = -1;
	}
	while (ft_isdigit(str[i]))
		a = (a * 10) + (str[i++] - 48) % 10;
	if (a > 2147483647 && (!(a == 2147483648 && n < 0)))
		ft_error(1);
	else if (i > 11 || (i == 11 && n != -1))
		ft_error(1);
}
