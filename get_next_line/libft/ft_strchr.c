#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i] != c && s[i] != '\0')
		i++;
	if (s[i] != '\0' || c == 0)
		return ((char *)&s[i]);
	else
		return (0);
}
