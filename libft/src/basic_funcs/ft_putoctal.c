#include "../../includes/libft.h"

void	ft_putoctal(unsigned int n)
{
	if (n < 8)
		ft_putchar(n + 48);
	else if (n >= 8)
	{
		ft_putoctal(n / 8);
		ft_putoctal(n % 8);
	}
}
