#include "../../includes/libft.h"

char	*ft_strtrim(char const *s)
{
	size_t	i;
	size_t	j;

	i = 0;
	while ((s[i] == ' ' || s[i] == '\t' || s[i] == '\n') && s[i] != '\0')
		i++;
	j = ft_strlen(s);
	while ((s[j] == ' ' || s[j] == '\t' || s[j] == '\n'
				|| s[j] == '\0') && j > i)
		j--;
	j++;
	return (ft_strsub(s, i, j - i));
}
