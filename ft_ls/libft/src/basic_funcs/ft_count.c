#include "../../includes/libft.h"

int		ft_count(char const *s, char c)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (s[i] != '\0')
	{
		while (s[i] == c)
			i++;
		if (s[i] != '\0' && s[i] != c)
			j++;
		while (s[i] != c && s[i] != '\0')
			i++;
	}
	return (j);
}
