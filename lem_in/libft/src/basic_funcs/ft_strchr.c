#include "../../includes/libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != c && s[i] != '\0')
			i++;
		if (s[i] != '\0')
			return ((char *)&s[i]);
	}
	return (NULL);
}
