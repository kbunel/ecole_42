#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		a;
	int		b;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != 0 && i < n)
		i++;
	if (i == n)
		i--;
	a = ((s1[i] - 48) <= 0 ? (s1[i] - 48) * -1 : s1[i] - 48);
	b = ((s2[i] - 48) <= 0 ? (s2[i] - 48) * -1 : s2[i] - 48);
	return (a - b);
}
